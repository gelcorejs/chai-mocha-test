describe('Sumas ', ()=>{
    //suma de positivo + positivo = positivo
    it('Suma de pos + pos = pos',() =>{
        console.log(2+2);
        console.log(4); 
    });
    //suma positivo mayor + negativo = positivo
    it('Suma de pos > + negativo = pos', ()=>{
        console.log(3+(-2));
        console.log(1); 
    });
});